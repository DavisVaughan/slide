# Look backwards until index[[i]] is past range_start, then add 1 to position
# Additional check to see if the start of our window is
# already outside the last value (no data here)
locate_window_start_positive <- function(position, i, range_start, finish, complete) {
  if (complete && range_start < i[[finish]]) {
    return(NA_integer_)
  }

  while (i[[position]] >= range_start) {
    if (position == finish) {
      return(position)
    }

    position <- position - 1L
  }

  # Always goes 1 too far, so add it back afterwards
  position + 1L
}

# Look forward until index[[i]] is at or past range_start
locate_window_start_negative <- function(position, i, range_start, finish) {
  if (range_start > i[[finish]]) {
    return(NA_integer_)
  }

  while (i[[position]] < range_start) {
    if (position == finish) {
      return(position)
    }

    position <- position + 1L
  }

  position
}

# Look forward until index[[i]] is past range_stop, then subtract 1 from position
locate_window_stop_positive <- function(position, i, range_stop, finish, complete) {
  if (complete && range_stop > i[[finish]]) {
    return(NA_integer_)
  }

  while (i[[position]] <= range_stop) {
    if (position == finish) {
      return(position)
    }

    position <- position + 1L
  }

  # Always goes 1 too far, so back it off afterwards
  position - 1L
}

# Look backwards until index[[i]] is at or past range_stop
# Additional check to see if the end of our window is
# already outside the first value (no data here)
locate_window_stop_negative <- function(position, i, range_stop, finish) {
  if (range_stop < i[[finish]]) {
    return(NA_integer_)
  }

  while (i[[position]] > range_stop) {
    if (position == finish) {
      return(position)
    }

    position <- position - 1L
  }

  position
}

check_na_range <- function(x, what) {
  if (is.na(x)) {
    abort(sprintf("`NA` value detected in the index range generated by `%s`.", what))
  }

  invisible(x)
}

# ------------------------------------------------------------------------------

locate_window_start <- function(position, i, range_start, n, positive, complete) {
  if (positive) {
    locate_window_start_positive(position, i, range_start, 1L, complete)
  } else {
    locate_window_start_negative(position, i, range_start, n)
  }
}

locate_window_stop <- function(position, i, range_stop, n, positive, complete) {
  if (positive) {
    locate_window_stop_positive(position, i, range_stop, n, complete)
  } else {
    locate_window_stop_negative(position, i, range_stop, 1L)
  }
}

# ------------------------------------------------------------------------------

compute_range_starts <- function(i, before) {
  if (is_function(before)) {
    out <- before(i)
    return(out)
  }

  i - before
}

compute_range_stops <- function(i, after) {
  if (is_function(after)) {
    out <- after(i)
    return(out)
  }

  i + after
}

# ------------------------------------------------------------------------------

is_range_start_positive <- function(i_position, range_start) {
  i_position >= range_start
}

is_range_stop_positive <- function(i_position, range_stop) {
  i_position <= range_stop
}

# ------------------------------------------------------------------------------

check_index_size <- function(n, i) {
  n_i <- vec_size(i)

  if (n != n_i) {
    abort(
      sprintf("The size of `.x` (%i) and `.i` (%i) must be the same.", n, n_i)
    )
  }

  invisible()
}

check_before_after <- function(params) {
  if (is_formula(params$before, scoped = TRUE, lhs = FALSE)) {
    params$before <- as_function(params$before)
  }

  if (is_formula(params$after, scoped = TRUE, lhs = FALSE)) {
    params$after <- as_function(params$after)
  }

  if (!is_function(params$before)) {
    vec_assert(params$before, size = 1L, arg = ".before")
  }

  if (!is_function(params$after)) {
    vec_assert(params$after, size = 1L, arg = ".after")
  }

  params
}

check_step <- function(params) {
  params$step <- vec_cast(params$step, integer(), x_arg = ".step")
  vec_assert(params$step, size = 1L, arg = ".step")
  params
}

check_complete <- function(params) {
  params$complete <- vec_cast(params$complete, logical(), x_arg = ".complete")
  vec_assert(params$complete, size = 1L, arg = ".complete")
  params
}

check_params <- function(params) {
  params <- check_before_after(params)
  params <- check_step(params)
  params <- check_complete(params)
  params
}

# ------------------------------------------------------------------------------

slide_index_impl <- function(.x,
                             .i,
                             .f,
                             ...,
                             .before,
                             .after,
                             .step,
                             .complete,
                             .constrain,
                             .ptype) {
  n <- vec_size(.x)
  .f <- as_function(.f)

  check_index_size(n, .i)

  params <- list(
    before = .before,
    after = .after,
    step = .step,
    complete = .complete
  )

  params <- check_params(params)

  range_starts <- compute_range_starts(.i, params$before)
  range_stops <- compute_range_stops(.i, params$after)

  position <- 1L
  entry <- 1L

  out <- vec_init(.ptype, n)

  while(position <= n) {
    i_position <- .i[[position]]
    range_start <- range_starts[[position]]
    range_stop <- range_stops[[position]]

    check_na_range(range_start, ".before")
    check_na_range(range_stop, ".after")

    range_start_positive <- is_range_start_positive(i_position, range_start)
    range_stop_positive <- is_range_stop_positive(i_position, range_stop)

    if (range_start > range_stop) {
      range_start <- as.character(range_start)
      range_stop <- as.character(range_stop)
      abort(sprintf("In iteration %i, the start of the range, %s, cannot be after the end of the range, %s.", position, range_start, range_stop))
    }

    window_start <- locate_window_start(position, .i, range_start, n, range_start_positive, params$complete)
    window_stop <- locate_window_stop(position, .i, range_stop, n, range_stop_positive, params$complete)

    if (is.na(window_start) || is.na(window_stop)) {
      position <- position + 1L
      entry <- entry + 1L
      next
    }

    slice <- vec_slice(.x, seq2(window_start, window_stop))

    elt <- .f(slice, ...)

    if (.constrain) {
      elt <- vec_cast(elt, .ptype)

      if (vec_size(elt) != 1L) {
        abort(sprintf("The size of each result of `.f` must be size 1. Iteration %i was size %i.", position, vec_size(elt)))
      }

      out <- vec_assign(out, entry, elt)
    } else {
      out[[entry]] <- elt
    }

    # Step forward
    position <- position + params$step
    entry <- entry + params$step
  }

  out
}

# ------------------------------------------------------------------------------

slide_index <- function(.x,
                        .i,
                        .f,
                        ...,
                        .before = 0L,
                        .after = 0L,
                        .step = 1L,
                        .complete = FALSE
                        ) {
  slide_index_impl(
    .x,
    .i,
    .f,
    ...,
    .before = .before,
    .after = .after,
    .step = .step,
    .complete = .complete,
    .constrain = FALSE,
    .ptype = list()
  )
}
